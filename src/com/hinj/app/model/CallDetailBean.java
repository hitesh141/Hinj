package com.hinj.app.model;

public class CallDetailBean {

	String id = "", call_time = "", duration = "", phone = "", created = "",
			direction = "",callUniqueId="";

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getCall_time() {
		return call_time;
	}

	public void setCall_time(String call_time) {
		this.call_time = call_time;
	}

	public String getDuration() {
		return duration;
	}

	public void setDuration(String duration) {
		this.duration = duration;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getCreated() {
		return created;
	}

	public void setCreated(String created) {
		this.created = created;
	}

	public String getDirection() {
		return direction;
	}

	public void setDirection(String direction) {
		this.direction = direction;
	}

	public String getCallUniqueId() {
		return callUniqueId;
	}

	public void setCallUniqueId(String callUniqueId) {
		this.callUniqueId = callUniqueId;
	}

	

	
}
